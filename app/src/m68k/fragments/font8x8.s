; ============================================================================
; font8x8.s — BASSM 8×8 Bitmap Font (ASCII 32–127)
; ============================================================================
;
; PURPOSE
;   Provides the bitmap glyph data for text rendering (_Text command, M6).
;   Each printable ASCII character is stored as 8 bytes — one byte per row,
;   top to bottom.  Within each byte, bit 7 (MSB) is the leftmost pixel,
;   matching the Amiga's planar bitplane convention.
;
; FONT PROPERTIES
;   Width  : 8 pixels per glyph
;   Height : 8 pixels per glyph
;   Range  : ASCII 32 (SPACE) through 127 (DEL — shown as blank)
;   Glyphs : 96 characters × 8 bytes = 768 bytes total
;
; ADDRESSING
;   Given a character code c (32 ≤ c ≤ 127):
;     glyph_address = _font8x8 + (c - 32) * 8
;   Each glyph is 8 consecutive bytes.
;
;   Assembly macro for callers:
;     moveq   #c,d0
;     subi.b  #32,d0
;     lsl.w   #3,d0          ; × 8
;     lea     _font8x8,a1
;     lea     (a1,d0.w),a1   ; a1 → glyph row 0
;
; PLACEMENT
;   SECTION font_data,DATA_C — chip RAM so the Blitter can use it as a
;   source channel.  The linker places DATA_C in Chip RAM automatically.
;
; USAGE (text.s, once implemented — M6)
;   _Text will iterate each character in the string, look up its glyph here,
;   and blit it into the back buffer using Blitter channels A (mask row) and
;   D (destination), minterm $CA = transparent OR:
;     D = (A AND C) OR ((NOT A) AND D)
;
; XDEF
;   _font8x8   — base address of font table
;
; ── REGISTER CONVENTION ─────────────────────────────────────────────────────
;   This file is data only — no code, no register assumptions.
; ============================================================================


